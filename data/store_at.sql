CREATE VIEW LocationView AS
SELECT storage.bookId     as bookId,
--        book.name      AS bookName,
       storage.buildingId AS buildingId,
       building.name      AS buildingName,
       storage.amount
FROM StoreAt storage
         INNER JOIN book ON storage.bookId = book.id
         INNER JOIN building on storage.buildingId = building.buildingid;

CREATE VIEW book_view AS
SELECT storage.bookId     AS book_id,
       book.name          AS book_name,
       book.isbn          AS isbn,
       book.coverurl      AS cover_url,
       storage.buildingId AS building_id,
       storage.amount     AS amount
--        building.name  AS buildingName, storage.amount
FROM StoreAt storage
         INNER JOIN book ON storage.bookId = book.id;
